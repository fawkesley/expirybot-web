from django.conf.urls import url

from .views import ExampleModelDetailView

UUID_PATTERN = (
    '[0-9a-fA-F]{8}-'
    '[0-9a-fA-F]{4}-'
    '[0-9a-fA-F]{4}-'
    '[0-9a-fA-F]{4}-'
    '[0-9a-fA-F]{12}'
)

urlpatterns = [
    url(
        r'^example_model/(?P<pk>' + UUID_PATTERN + ')/$',
        ExampleModelDetailView.as_view(),
        name='example-model-detail'
    ),

]
